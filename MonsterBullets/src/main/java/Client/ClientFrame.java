    private void generateBoard() {
    // Set layout for the panel containing the board
        JPanel boardPanel = new JPanel(new GridLayout(DIMENSIONES, DIMENSIONES));

        for (int row = 0; row < DIMENSIONES; row++) {
            for (int col = 0; col < DIMENSIONES; col++) {

                // coloca imagen a todos vacio
                tableroLabels[row][col] = new JLabel(square);
                tableroLabels[3][5] = new JLabel(common);
                tableroLabels[10][10] = new JLabel(golden);
                tableroLabels[12][1] = new JLabel(tailed);
                tableroLabels[3][4] = new JLabel(angry);
                tableroLabels[row][col] = new JLabel(square);
                jPanel1.add(tableroLabels[row][col]);
                jPanel1.add(tableroLabels[3][5]);
                jPanel1.add(tableroLabels[10][10]);
                jPanel1.add(tableroLabels[12][1]);
                // coloca dimensiones y localidad
                tableroLabels[row][col].setBounds(50+25*row, 50+25*col, 30, 20);
                tableroLogico[row][col]=0;
            }
        }
        
        
        // Add the board panel to the main frame
        getContentPane().add(boardPanel, BorderLayout.CENTER);
        pack();
    }
    

